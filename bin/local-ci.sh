#!/usr/bin/env bash
# bin/local-ci.sh — local-CI gate for WB Gamification.
#
# Single entry point that runs every quality + correctness + journey
# check in the right order. Designed to be runnable from a fresh clone
# without GitHub Actions or any external infrastructure.
#
# Usage:
#   composer ci                         # via composer script
#   bash bin/local-ci.sh                # direct, all stages
#   bash bin/local-ci.sh --no-journeys  # skip browser-dependent stages
#   bash bin/local-ci.sh --quick        # lint + coding-rules only (~10s)
#
# Exit 0 = green; non-zero = at least one stage failed (the offending
# stage's stdout/stderr was already printed). Each stage has a numeric
# tag (1.x = static, 2.x = security, 3.x = manifest, 4.x = journeys)
# so you can scroll back to the first red.
#
# Generated by wp-plugin-onboard skill. Edit freely — this is your
# plugin's CI orchestrator, not skill state.

set -uo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
PLUGIN_SLUG="$(basename "$PLUGIN_DIR")"

cd "$PLUGIN_DIR"

MODE="full"
SITE_URL="${LOCAL_CI_SITE_URL:-http://wb-gamification.local}"

while [ $# -gt 0 ]; do
  case "$1" in
    --no-journeys) MODE="no-journeys" ;;
    --quick)       MODE="quick" ;;
    --site)        SITE_URL="$2"; shift ;;
    *) echo "Unknown flag: $1"; exit 2 ;;
  esac
  shift
done

# ─── helpers ─────────────────────────────────────────────────────────────────

FAILED=()
GREEN="\033[0;32m"; RED="\033[0;31m"; YELLOW="\033[0;33m"; DIM="\033[2m"; RESET="\033[0m"

step() { printf "${DIM}[%s]${RESET} %s\n" "$1" "${@:2}"; }
pass() { printf "${GREEN}✓${RESET}  %s\n" "$*"; }
fail() { printf "${RED}✗${RESET}  %s\n" "$*"; FAILED+=("$1"); }
warn() { printf "${YELLOW}!${RESET}  %s\n" "$*"; }

run_stage() {
  local tag="$1"; local label="$2"; shift 2
  step "$tag" "$label"
  if "$@" > /tmp/local-ci-$$.log 2>&1; then
    pass "$label"
  else
    fail "$tag $label"
    cat /tmp/local-ci-$$.log | sed 's/^/    /'
  fi
  rm -f /tmp/local-ci-$$.log
}

echo "=== WB Gamification local-CI ==="
echo "Mode: $MODE  ·  Site: $SITE_URL"
echo ""

# ─── 1.x — Static checks (fast, no runtime needed) ───────────────────────────

step "1.1" "PHP lint (every changed-source PHP file)"
PHP_LINT_FAILED=0
while IFS= read -r -d '' file; do
  if ! php -l "$file" > /dev/null 2>&1; then
    fail "1.1 PHP lint: $file"
    php -l "$file" 2>&1 | sed 's/^/    /'
    PHP_LINT_FAILED=1
  fi
done < <(find . -type f -name '*.php' \
          -not -path './vendor/*' \
          -not -path './node_modules/*' \
          -not -path './tests/*' \
          -not -path './build/*' \
          -not -path './dist/*' \
          -not -path './.git/*' \
          -print0)
[ "$PHP_LINT_FAILED" = "0" ] && pass "PHP lint clean"

if [ "$MODE" = "quick" ]; then
  step "QUICK" "skipping WPCS / PHPStan / PHPUnit / wppqa / journeys"
else
  # WPCS, PHPStan, PHPUnit are non-negotiable in non-quick mode. Force-fail
  # the build when vendor/ is missing — silent skips hid PHPUnit failures
  # for weeks before the 2026-05-27 audit caught it (3 stale tests).
  # Bypass: SKIP_VENDOR_CHECK=1 (only when bootstrapping a brand-new clone).
  if [ -f phpcs.xml ] || [ -f phpcs.xml.dist ] || [ -f .phpcs.xml ] || [ -f .phpcs.xml.dist ]; then
    if [ -x vendor/bin/phpcs ]; then
      run_stage "1.2" "WPCS (phpcs)" composer phpcs
    elif [ "${SKIP_VENDOR_CHECK:-0}" = "1" ]; then
      warn "1.2 WPCS skipped — SKIP_VENDOR_CHECK=1 set"
    else
      fail "1.2 WPCS — vendor/bin/phpcs missing. Run 'composer install'."
    fi
  fi

  if [ -f phpstan.neon ] || [ -f phpstan.neon.dist ]; then
    if [ -x vendor/bin/phpstan ]; then
      run_stage "1.3" "PHPStan static analysis" composer phpstan
    elif [ "${SKIP_VENDOR_CHECK:-0}" = "1" ]; then
      warn "1.3 PHPStan skipped — SKIP_VENDOR_CHECK=1 set"
    else
      fail "1.3 PHPStan — vendor/bin/phpstan missing. Run 'composer install'."
    fi
  fi

  # PHPUnit — runs after WPCS + PHPStan so syntax/type failures surface
  # before behaviour failures. Added 2026-05-27 in response to the
  # stability audit finding that the suite had been silently red for
  # weeks (3 stale-fixture failures in ShortcodeHandlerTest +
  # RedemptionEngineTest that no gate caught).
  if [ -f phpunit.xml.dist ] || [ -f phpunit.xml ]; then
    if [ -x vendor/bin/phpunit ]; then
      run_stage "1.5" "PHPUnit (composer test)" composer test
    elif [ "${SKIP_VENDOR_CHECK:-0}" = "1" ]; then
      warn "1.5 PHPUnit skipped — SKIP_VENDOR_CHECK=1 set"
    else
      fail "1.5 PHPUnit — vendor/bin/phpunit missing. Run 'composer install'."
    fi
  fi

  if [ -f package.json ] && grep -q '"build"' package.json; then
    if [ -d node_modules ]; then
      run_stage "1.4" "JS build (npm run build)" npm run build
    elif [ "${SKIP_VENDOR_CHECK:-0}" = "1" ]; then
      warn "1.4 JS build skipped — SKIP_VENDOR_CHECK=1 set"
    else
      fail "1.4 JS build — node_modules missing. Run 'npm install'."
    fi
  fi
fi

# ─── 2.x — Security + Architecture (always cheap) ────────────────────────────

if [ -x bin/coding-rules-check.sh ]; then
  run_stage "2.1" "Coding-rules check" bash bin/coding-rules-check.sh
fi

if [ -x bin/architecture-checks.sh ]; then
  run_stage "2.2" "Architecture invariants (Free/Pro contract)" bash bin/architecture-checks.sh
fi

if [ -x bin/check-block-standard.sh ]; then
  run_stage "2.3" "Wbcom Block Quality Standard" bash bin/check-block-standard.sh
fi

# 2.4 — UX audit (ux-foundation compliance).
# Vendored from ~/.claude/skills/ux-audit/templates/ux-audit.sh on 2026-05-26.
# Block-severity violations fail the build; advisory rows ship with next PR.
if [ -x bin/ux-audit.sh ]; then
  step "2.4" "UX audit (ux-foundation compliance)"
  if PREFIX=wb-gam bash bin/ux-audit.sh "$PLUGIN_DIR" > /tmp/local-ci-ux-$$.log 2>&1; then
    pass "UX audit — no block-severity violations"
  else
    fail "2.4 UX audit"
    cat /tmp/local-ci-ux-$$.log | sed 's/^/    /'
  fi
  rm -f /tmp/local-ci-ux-$$.log
fi

# 2.5 — Plugin-development rules (portfolio-wide gates from
# the wp-plugin-development skill: no jQuery on frontend, no admin-ajax
# on customer surfaces, every block declares the wb-gam-tokens style dep,
# every block ships its own style.css, BP integrations boot on bp_loaded).
if [ -x bin/plugin-dev-rules-check.sh ]; then
  run_stage "2.5" "Plugin-dev rules (wp-plugin-development gates)" bash bin/plugin-dev-rules-check.sh
fi

# 2.6 — wppqa baseline freshness (output of the wp-plugin-qa MCP checks).
# Validates audit/wppqa-baseline-LATEST/SUMMARY.md exists, is ≤30d old, and
# reports failed=0. Refresh via /wp-plugin-onboard --refresh in Claude.
if [ -x bin/wppqa-baseline-check.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.6" "wppqa baseline (wp-plugin-qa MCP)" bash bin/wppqa-baseline-check.sh
fi

# 2.7 — enum-drift gate. Cross-layer contract check: every controller's
# VALID_* enum must align with the engine that consumes the value. Added
# 2026-05-27 in response to the stability audit — caught a real
# `point_multiplier` vs `points_multiplier` typo on first run. See
# audit/STABILITY-2026-05-27.md §4.D.
if [ -x bin/check-enum-drift.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.7" "Enum drift (cross-layer contract)" bash bin/check-enum-drift.sh
fi

# 2.8 — CSS-class orphan gate. Every wbgam-*/wb-gam-* class emitted by a
# PHP template must have a matching CSS selector somewhere. Catches the
# Basecamp #9925205802 Issue 2 bug class — Setup Wizard switch was
# invisible because PHP wrote `__slider` and CSS only knew `__track`.
# Baseline-driven (audit/css-orphan-baseline.txt); fails only on NEW
# orphans. Refresh: bin/check-css-orphans.sh --update-baseline.
if [ -x bin/check-css-orphans.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.8" "CSS orphan classes (PHP → CSS contract)" bash bin/check-css-orphans.sh
fi

# 2.9 — Action sync/async manifest invariant. Every action manifest entry
# with repeatable=true must declare async explicitly — default-routing
# was the root cause of Basecamp #9925589914 (WC events queued through
# Action Scheduler when admins expected immediate awards). Baseline at
# audit/action-async-baseline.txt; refresh with --update-baseline.
if [ -x bin/check-action-async.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.9" "Action manifest sync/async invariant" bash bin/check-action-async.sh
fi

# 2.10 — Critical event-wiring gate. Hardcoded list of internal events
# that MUST have both a firer and a listener inside this plugin. Catches
# the Basecamp #9927383947 bug class — RedemptionEngine fired
# `wb_gam_points_redeemed` but TransactionalEmailEngine forgot to
# subscribe. Extension-point hooks (consumed by third parties) are NOT
# in scope; add new entries to CRITICAL_EVENTS in the script.
if [ -x bin/check-event-wiring.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.10" "Critical event wiring" bash bin/check-event-wiring.sh
fi

# 2.11 — Coverage floor. Only fires when build/coverage/coverage.txt is
# present (produced by composer test:coverage). Default ci/ci:no-journeys
# runs use --no-coverage for speed and silently skip this stage. CI runs
# that explicitly opt into coverage (composer test:coverage) chain the
# floor check after the report is written. Audit baseline at level 9
# PHPStan + 109 PHPUnit / 3.79% lines / 5.60% methods (2026-05-27).
if [ -x bin/check-coverage-floor.sh ] && [ -f build/coverage/coverage.txt ] && [ "$MODE" != "quick" ]; then
  run_stage "2.11" "PHPUnit coverage floor" bash bin/check-coverage-floor.sh
fi

# 2.12 — WordPress.org Plugin Check. The submission bar: 0 ERRORs in
# shipped code (src/, integrations/, wb-gamification.php). WARNINGs are
# tracked against a baseline so the count can't grow. Wired in
# 2026-05-27 after fixing 3 ERRORs (1 was from the wizard refactor in
# the same session). Refresh: bin/check-plugin-check.sh --update-baseline.
if [ -x bin/check-plugin-check.sh ] && [ "$MODE" != "quick" ]; then
  run_stage "2.12" "WP.org Plugin Check (0 errors in shipped code)" bash bin/check-plugin-check.sh
fi

# ─── 3.x — Manifest freshness ────────────────────────────────────────────────

if [ "$MODE" != "quick" ]; then
  if [ -f audit/manifest.json ]; then
    if jq . audit/manifest.json > /dev/null 2>&1; then
      pass "3.1 Manifest is valid JSON"

      MANIFEST_AT="$(jq -r '.generated.at // empty' audit/manifest.json)"
      if [ -n "$MANIFEST_AT" ]; then
        AGE_DAYS=$(( ($(date -u +%s) - $(date -juf "%Y-%m-%dT%H:%M:%SZ" "$MANIFEST_AT" +%s 2>/dev/null || echo 0)) / 86400 ))
        if [ "$AGE_DAYS" -gt 30 ]; then
          warn "3.1 Manifest is ${AGE_DAYS}d old — refresh via /wp-plugin-onboard --refresh"
        fi
      fi
    else
      fail "3.1 Manifest JSON invalid"
    fi
  else
    warn "3.1 No audit/manifest.json — run /wp-plugin-onboard"
  fi
fi

# ─── 4.x — Journeys (browser-dependent, can be skipped) ──────────────────────

if [ "$MODE" = "full" ]; then
  if [ -d audit/journeys ] && [ -x bin/run-journeys.sh ]; then
    if curl -fsS "$SITE_URL" > /dev/null 2>&1; then
      run_stage "4.1" "Customer journeys (browser+REST+DB)" bash bin/run-journeys.sh --site "$SITE_URL"
    else
      warn "4.1 Journeys skipped — site $SITE_URL is not reachable"
    fi
  fi
fi

# ─── Summary ─────────────────────────────────────────────────────────────────

echo ""
echo "=== Summary ==="
if [ ${#FAILED[@]} -eq 0 ]; then
  printf "${GREEN}local-CI green${RESET}\n"
  exit 0
else
  printf "${RED}local-CI failed: %d stage(s)${RESET}\n" "${#FAILED[@]}"
  for stage in "${FAILED[@]}"; do
    printf "  ${RED}✗${RESET} %s\n" "$stage"
  done
  exit 1
fi
